// Cria os procedimentos padrão (isDefault=true) para um usuário, pulando os
// que já existirem com o mesmo nome (evita duplicar em reexecuções).
export async function seedDefaultProcedures(prisma, userId) {
  const existing = await prisma.procedure.findMany({
    where: { userId },
    select: { name: true },
  });
  const have = new Set(existing.map((p) => p.name.trim().toLowerCase()));
  const toCreate = DEFAULT_PROCEDURES.filter(
    (p) => !have.has(p.name.trim().toLowerCase())
  );
  if (toCreate.length === 0) return { created: 0 };
  await prisma.procedure.createMany({
    data: toCreate.map((p) => ({ ...p, userId, isDefault: true, isActive: true })),
  });
  return { created: toCreate.length };
}
